const checkShopping = (text) => {
    const shoppingTitle = text.match(/^[\s-]*\S+:/)?.[0];
    const isShopping = shoppingTitle && !shoppingTitle.match(/http/i);
    return isShopping;
};

// =================================================================================================================
//  E X P O R T
// =================================================================================================================
export default checkShopping;
