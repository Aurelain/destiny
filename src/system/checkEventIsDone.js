import {DONE_MATCH} from '../SETTINGS.js';

// =================================================================================================================
//  P U B L I C
// =================================================================================================================
/**
 *
 */
const checkEventIsDone = (event, forcedDone = {}) => {
    const {id, summary, status} = event;
    if (id in forcedDone) {
        return true;
    }
    return Boolean(summary.match(DONE_MATCH)) || status === 'cancelled';
};

// =================================================================================================================
//  E X P O R T
// =================================================================================================================
export default checkEventIsDone;
