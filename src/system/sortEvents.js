/**
 *
 */
const sortEvents = (events) => {
    events.sort(compareEvents);
};

// =====================================================================================================================
//  P R I V A T E
// =====================================================================================================================
/**
 *
 */
const compareEvents = (a, b) => {
    if (a.start < b.start) {
        return -1;
    } else if (a.start > b.start) {
        return 1;
    } else {
        if (a.summary < b.summary) {
            return -1;
        } else if (a.summary > b.summary) {
            return 1;
        } else {
            return 0;
        }
    }
};

// =====================================================================================================================
//  E X P O R T
// =====================================================================================================================
export default sortEvents;
