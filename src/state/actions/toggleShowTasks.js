import {selectOptions} from '../selectors.js';
import {setState} from '../store.js';

// =====================================================================================================================
//  P U B L I C
// =====================================================================================================================
/**
 *
 */
const toggleShowTasks = () => {
    setState((state) => {
        const options = selectOptions(state);
        options.showTasks = !options.showTasks;
    });
};

// =====================================================================================================================
//  E X P O R T
// =====================================================================================================================
export default toggleShowTasks;
