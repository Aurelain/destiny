import {selectOptions} from '../selectors.js';
import {setState} from '../store.js';

// =====================================================================================================================
//  P U B L I C
// =====================================================================================================================
/**
 *
 */
const toggleShowDone = () => {
    setState((state) => {
        const options = selectOptions(state);
        options.showDone = !options.showDone;
    });
};

// =====================================================================================================================
//  E X P O R T
// =====================================================================================================================
export default toggleShowDone;
