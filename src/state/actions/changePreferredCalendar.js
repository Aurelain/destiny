import {selectOptions} from '../selectors.js';
import {setState} from '../store.js';

// =====================================================================================================================
//  P U B L I C
// =====================================================================================================================
/**
 *
 */
const changePreferredCalendar = (calendarId) => {
    setState((state) => {
        const options = selectOptions(state);
        options.preferredCalendar = calendarId;
    });
};

// =====================================================================================================================
//  E X P O R T
// =====================================================================================================================
export default changePreferredCalendar;
