const getYYYYMMDD = (date) => {
    date = date || new Date();
    const dateString = typeof date === 'string' ? date : date.toISOString();
    return dateString.substring(0, 10);
};

// =================================================================================================================
//  E X P O R T
// =================================================================================================================
export default getYYYYMMDD;
