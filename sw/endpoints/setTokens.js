import localforage from 'localforage';

// =====================================================================================================================
//  P U B L I C
// =====================================================================================================================
/**
 *
 */
const setTokens = async (request) => {
    const tokens = await request.json();
    console.log('tokens in localforage:', tokens);
    await localforage.setItem('destiny-tokens', tokens);
    return true;
};

// =====================================================================================================================
//  E X P O R T
// =====================================================================================================================
export default setTokens;
